
package Utils.Data;

public class Season {
    private final int id;
    private final int numberSeason;

    public Season(int id, int numberSeason) {
        this.id = id;
        this.numberSeason = numberSeason;
    }

    public int getId() {
        return id;
    }

    public int getNumberSeason() {
        return numberSeason;
    }
    
    
}
